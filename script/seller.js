let CONVENIENCE_FEES = 99;
let bagItemObjects;
onLoad();

function onLoad() {
  loadBagItemObjects();
  displayBagItems();
  displayBagSummary();
}

function displayBagSummary() {
  let bagSummaryElement = document.querySelector('.bag-summary');
  let totalItem = bagItemObjects.length;
  let totalMRP = 0;
  let totalDiscount = 0;

  bagItemObjects.forEach(bagItem => {
    totalMRP += bagItem.original_price;
    totalDiscount += bagItem.original_price - bagItem.current_price;
  });

  if(totalMRP==0){
    CONVENIENCE_FEES=0;
  }

  let finalPayment = totalMRP - totalDiscount - CONVENIENCE_FEES;
  let commission_amount = finalPayment * 0.05;
  commission_amount = commission_amount.toFixed(2)
  let getamount = finalPayment - commission_amount;


  bagSummaryElement.innerHTML = `
    <div class="bag-details-container">
    <div class="price-header">PRICE DETAILS (${totalItem} Items) </div>
    <div class="price-item">
      <span class="price-item-tag">Total MRP</span>
      <span class="price-item-value">₹${totalMRP}</span>
    </div>
    <div class="price-item">
      <span class="price-item-tag">Discount on MRP</span>
      <span class="price-item-value priceDetail-base-discount">-₹${totalDiscount}</span>
    </div>
    <div class="price-item">
      <span class="price-item-tag">Convenience Fee</span>
      <span class="price-item-value">- ₹${CONVENIENCE_FEES}</span>
    </div>
    <hr>
    <div class="price-footer">
      <span class="price-item-tag">Total Amount</span>
      <span class="price-item-value">₹${finalPayment}</span>
    </div>
    <div class="price-footer">
      <span class="price-item-tag">Platform Commission Amount</span>
      <span class="price-item-value">- ₹${commission_amount}</span>
    </div>

    <div class="price-footer">
      <span class="price-item-tag">Total Amount Recieved</span>
      <span class="price-item-value"> ₹${getamount}</span>
    </div>
  </div>
  <button class="btn-place-order" onclick = "confirm_and_dlt();">
    <div class="css-xjhrni">CONFIRM ORDER</div>
  </button>
  `;
}

function confirm_and_dlt(){
    localStorage.clear();
    window.open(
        "seller_confirm.html", 
        "_self"
    );
}

